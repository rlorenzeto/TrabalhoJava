public class Professor {
	private String nome, endereco, email, telefone, formacaoAcademica, numeroRegistro;
	private int idade;
	
	public Professor(String nome, String endereco, String email, String telefone, String formacaoAcademica, String numeroRegistro, int idade) {
		this.nome = nome;
		this.endereco = endereco;
		this.email = email;
		this.telefone = telefone;
		this.formacaoAcademica = formacaoAcademica;
		this.numeroRegistro = numeroRegistro;
		this.idade = idade;
	}
	
	
}
