public class Turma {
	private int numero;
	private Aluno alunos;
	private Disciplina disciplina;
	private Horario horario;
	
	public Turma(int numero, Aluno alunos, Disciplina disciplina, Horario horario) {
		this.numero = numero;
		this.alunos = alunos;
		this.disciplina = disciplina;
		this.horario = horario;
	}
	
	
}
